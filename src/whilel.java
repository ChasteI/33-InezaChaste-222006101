//defining the class named whilel 
public class whilel {
    // the main method
    public static void main(String[] args) { 
        //declaration and initialization 
        int d=1;  
        //start the while loop with the condition
        while(d<=7){  
            //print the value of d
            System.out.println(d);  
            //incrementation
        d++;  
        }  
    }  
}
