//this line declares a public class called eforloop
public class eforloop {
    //this line declares a public static method called main,it is the entry point of the program
    public static void main(String[] args) { 
        //this line declares an integer variable called j and initialize it to 1
          //starting point of for loop 
        for(int j=1;j<=5;j++){ 
            //the line prints the value of j to the console
            System.out.println(j);  

        }  
    } 
}
